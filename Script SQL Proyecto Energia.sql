-- ============================================================
--  CONSULTAS PRINCIPALES
--  Proyecto Nacional de Fibra Óptica - Colombia
-- ============================================================

USE fibra_optica_colombia;


-- ============================================================
-- 1. RESUMEN GENERAL
-- ============================================================

-- Total de municipios, inversión acumulada y promedio por proyecto
SELECT
    COUNT(id_proyecto)                        AS total_proyectos,
    SUM(inversion_total)                      AS inversion_total_cop,
    AVG(inversion_total)                      AS inversion_promedio_cop,
    SUM(inversion_fontic)                     AS total_fontic_cop,
    SUM(inversion_contrapartida)              AS total_contrapartida_cop,
    ROUND(SUM(inversion_fontic)
        / NULLIF(SUM(inversion_total),0)*100, 2) AS pct_fontic,
    ROUND(SUM(inversion_contrapartida)
        / NULLIF(SUM(inversion_total),0)*100, 2) AS pct_contrapartida
FROM proyecto_fibra;


-- ============================================================
-- 2. ANÁLISIS POR REGIÓN
-- ============================================================

-- Municipios e inversión total por región, ordenado de mayor a menor
SELECT
    r.nombre_region,
    COUNT(pf.id_proyecto)           AS municipios_cubiertos,
    FORMAT(SUM(pf.inversion_total), 0)  AS inversion_total_cop,
    FORMAT(AVG(pf.inversion_total), 0)  AS inversion_promedio_cop,
    ROUND(SUM(pf.inversion_fontic)
        / NULLIF(SUM(pf.inversion_total),0)*100, 1) AS pct_fontic
FROM proyecto_fibra pf
JOIN municipio    m  ON pf.cod_municipio   = m.cod_municipio
JOIN departamento d  ON m.cod_departamento = d.cod_departamento
JOIN region       r  ON d.id_region        = r.id_region
GROUP BY r.id_region, r.nombre_region
ORDER BY SUM(pf.inversion_total) DESC;


-- ============================================================
-- 3. ANÁLISIS POR DEPARTAMENTO
-- ============================================================

-- Cobertura y financiación por departamento
SELECT
    d.cod_departamento,
    d.nombre                            AS departamento,
    r.nombre_region,
    COUNT(pf.id_proyecto)               AS municipios_cubiertos,
    FORMAT(SUM(pf.inversion_total),0)   AS inversion_total_cop,
    FORMAT(SUM(pf.inversion_fontic),0)  AS fontic_cop,
    FORMAT(SUM(pf.inversion_contrapartida),0) AS contrapartida_cop
FROM proyecto_fibra pf
JOIN municipio    m  ON pf.cod_municipio   = m.cod_municipio
JOIN departamento d  ON m.cod_departamento = d.cod_departamento
JOIN region       r  ON d.id_region        = r.id_region
GROUP BY d.cod_departamento, d.nombre, r.nombre_region
ORDER BY SUM(pf.inversion_total) DESC;


-- ============================================================
-- 4. TOP 10 MUNICIPIOS POR INVERSIÓN
-- ============================================================

SELECT
    m.cod_municipio,
    m.nombre                            AS municipio,
    d.nombre                            AS departamento,
    r.nombre_region,
    FORMAT(pf.inversion_total, 0)       AS inversion_total_cop,
    FORMAT(pf.inversion_fontic, 0)      AS fontic_cop,
    FORMAT(pf.inversion_contrapartida, 0) AS contrapartida_cop,
    pf.fecha_operacion,
    pf.fecha_fin_operacion
FROM proyecto_fibra pf
JOIN municipio    m  ON pf.cod_municipio   = m.cod_municipio
JOIN departamento d  ON m.cod_departamento = d.cod_departamento
JOIN region       r  ON d.id_region        = r.id_region
ORDER BY pf.inversion_total DESC
LIMIT 10;


-- ============================================================
-- 5. PROYECTOS POR ESTADO Y VIGENCIA
-- ============================================================

-- Distribución de estados actuales
SELECT
    estado_actual,
    COUNT(*) AS cantidad,
    ROUND(COUNT(*) * 100.0 / (SELECT COUNT(*) FROM proyecto_fibra), 2) AS porcentaje
FROM proyecto_fibra
GROUP BY estado_actual
ORDER BY cantidad DESC;

-- Proyectos con vigencia próxima a vencer (en los próximos 365 días)
SELECT
    m.nombre                        AS municipio,
    d.nombre                        AS departamento,
    pf.fecha_fin_operacion,
    DATEDIFF(pf.fecha_fin_operacion, CURDATE()) AS dias_restantes,
    FORMAT(pf.inversion_total, 0)   AS inversion_total_cop
FROM proyecto_fibra pf
JOIN municipio    m  ON pf.cod_municipio   = m.cod_municipio
JOIN departamento d  ON m.cod_departamento = d.cod_departamento
WHERE pf.fecha_fin_operacion BETWEEN CURDATE() AND DATE_ADD(CURDATE(), INTERVAL 365 DAY)
ORDER BY pf.fecha_fin_operacion ASC;

-- Proyectos ya vencidos (fecha_fin_operacion en el pasado)
SELECT
    m.nombre                      AS municipio,
    d.nombre                      AS departamento,
    pf.fecha_fin_operacion,
    ABS(DATEDIFF(pf.fecha_fin_operacion, CURDATE())) AS dias_vencido,
    FORMAT(pf.inversion_total, 0) AS inversion_total_cop
FROM proyecto_fibra pf
JOIN municipio    m  ON pf.cod_municipio   = m.cod_municipio
JOIN departamento d  ON m.cod_departamento = d.cod_departamento
WHERE pf.fecha_fin_operacion < CURDATE()
ORDER BY pf.fecha_fin_operacion ASC;


-- ============================================================
-- 6. ANÁLISIS DE FINANCIACIÓN
-- ============================================================

-- Municipios donde la contrapartida supera a FONTIC
SELECT
    m.nombre                                AS municipio,
    d.nombre                                AS departamento,
    FORMAT(pf.inversion_fontic, 0)          AS fontic_cop,
    FORMAT(pf.inversion_contrapartida, 0)   AS contrapartida_cop,
    FORMAT(pf.inversion_total, 0)           AS total_cop,
    ROUND(pf.inversion_contrapartida
        / NULLIF(pf.inversion_total,0)*100, 1) AS pct_contrapartida
FROM proyecto_fibra pf
JOIN municipio    m  ON pf.cod_municipio   = m.cod_municipio
JOIN departamento d  ON m.cod_departamento = d.cod_departamento
WHERE pf.inversion_contrapartida > pf.inversion_fontic
ORDER BY pf.inversion_contrapartida DESC;

-- Proyectos sin inversión de contrapartida (100% FONTIC)
SELECT
    m.nombre                        AS municipio,
    d.nombre                        AS departamento,
    FORMAT(pf.inversion_fontic, 0)  AS fontic_cop
FROM proyecto_fibra pf
JOIN municipio    m  ON pf.cod_municipio   = m.cod_municipio
JOIN departamento d  ON m.cod_departamento = d.cod_departamento
WHERE pf.inversion_contrapartida = 0
   OR pf.inversion_contrapartida IS NULL
ORDER BY pf.inversion_fontic DESC;


-- ============================================================
-- 7. ANÁLISIS TEMPORAL
-- ============================================================

-- Proyectos iniciados por año
SELECT
    YEAR(fecha_operacion)           AS anio_inicio,
    COUNT(*)                        AS proyectos_iniciados,
    FORMAT(SUM(inversion_total), 0) AS inversion_total_cop
FROM proyecto_fibra
WHERE fecha_operacion IS NOT NULL
GROUP BY YEAR(fecha_operacion)
ORDER BY anio_inicio;

-- Proyectos que finalizan por año
SELECT
    YEAR(fecha_fin_operacion)       AS anio_fin,
    COUNT(*)                        AS proyectos_por_vencer,
    FORMAT(SUM(inversion_total), 0) AS inversion_en_juego_cop
FROM proyecto_fibra
WHERE fecha_fin_operacion IS NOT NULL
GROUP BY YEAR(fecha_fin_operacion)
ORDER BY anio_fin;


-- ============================================================
-- 8. BÚSQUEDA Y DETALLE
-- ============================================================

-- Detalle completo de un municipio específico (reemplaza el nombre)
SELECT
    r.nombre_region,
    d.nombre                                  AS departamento,
    m.nombre                                  AS municipio,
    m.cod_municipio,
    pf.estado_actual,
    pf.fecha_operacion,
    pf.fecha_fin_operacion,
    DATEDIFF(pf.fecha_fin_operacion, pf.fecha_operacion) AS dias_vigencia,
    FORMAT(pf.inversion_fontic, 0)            AS fontic_cop,
    FORMAT(pf.inversion_contrapartida, 0)     AS contrapartida_cop,
    FORMAT(pf.inversion_total, 0)             AS total_cop,
    pf.fecha_cargue,
    pf.fecha_vigencia,
    i.descripcion                             AS indicador
FROM proyecto_fibra pf
JOIN municipio    m  ON pf.cod_municipio   = m.cod_municipio
JOIN departamento d  ON m.cod_departamento = d.cod_departamento
JOIN region       r  ON d.id_region        = r.id_region
JOIN indicador    i  ON pf.id_indicador    = i.id_indicador
WHERE m.nombre LIKE '%MARMATO%';   -- <- cambia el nombre aquí

-- Buscar todos los municipios de un departamento
SELECT
    m.cod_municipio,
    m.nombre                        AS municipio,
    pf.estado_actual,
    FORMAT(pf.inversion_total, 0)   AS inversion_total_cop,
    pf.fecha_operacion,
    pf.fecha_fin_operacion
FROM municipio    m
JOIN departamento d  ON m.cod_departamento = d.cod_departamento
JOIN proyecto_fibra pf ON pf.cod_municipio = m.cod_municipio
WHERE d.nombre = 'QUINDIO'      -- <- cambia el departamento aquí
ORDER BY pf.inversion_total DESC;


-- ============================================================
-- 9. RANKING Y PERCENTILES
-- ============================================================

-- Ranking de municipios por inversión dentro de su región
SELECT
    r.nombre_region,
    m.nombre                        AS municipio,
    d.nombre                        AS departamento,
    FORMAT(pf.inversion_total, 0)   AS inversion_total_cop,
    RANK() OVER (
        PARTITION BY r.id_region
        ORDER BY pf.inversion_total DESC
    )                               AS ranking_en_region
FROM proyecto_fibra pf
JOIN municipio    m  ON pf.cod_municipio   = m.cod_municipio
JOIN departamento d  ON m.cod_departamento = d.cod_departamento
JOIN region       r  ON d.id_region        = r.id_region
ORDER BY r.nombre_region, ranking_en_region;

-- Clasificar municipios por nivel de inversión (alto / medio / bajo)
SELECT
    m.nombre                        AS municipio,
    d.nombre                        AS departamento,
    FORMAT(pf.inversion_total, 0)   AS inversion_total_cop,
    CASE
        WHEN pf.inversion_total >= 1500000000 THEN 'Alta'
        WHEN pf.inversion_total >= 800000000  THEN 'Media'
        ELSE 'Baja'
    END                             AS nivel_inversion
FROM proyecto_fibra pf
JOIN municipio    m  ON pf.cod_municipio   = m.cod_municipio
JOIN departamento d  ON m.cod_departamento = d.cod_departamento
ORDER BY pf.inversion_total DESC;


-- ============================================================
-- 10. CONTROL DE CALIDAD
-- ============================================================

-- Registros con valores nulos en campos clave
SELECT
    SUM(inversion_total IS NULL OR inversion_total = 0) AS sin_inversion_total,
    SUM(inversion_fontic IS NULL) AS sin_fontic,
    SUM(fecha_operacion IS NULL) AS sin_fecha_inicio,
    SUM(fecha_fin_operacion IS NULL) AS sin_fecha_fin,
    SUM(estado_actual IS NULL OR estado_actual = '') AS sin_estado
FROM proyecto_fibra;

-- Verificar consistencia: inversion_total = fontic + contrapartida
SELECT
    m.nombre                                            AS municipio,
    FORMAT(pf.inversion_fontic, 0)                      AS fontic,
    FORMAT(pf.inversion_contrapartida, 0)               AS contrapartida,
    FORMAT(pf.inversion_total, 0)                       AS total_declarado,
    FORMAT(pf.inversion_fontic + pf.inversion_contrapartida, 0) AS total_calculado,
    ABS(pf.inversion_total
        - (pf.inversion_fontic + pf.inversion_contrapartida))   AS diferencia
FROM proyecto_fibra pf
JOIN municipio m ON pf.cod_municipio = m.cod_municipio
WHERE ABS(pf.inversion_total
        - (pf.inversion_fontic + pf.inversion_contrapartida)) > 1
ORDER BY diferencia DESC;