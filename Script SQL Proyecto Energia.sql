CREATE DATABASE IF NOT EXISTS fibra_optica_colombia
  CHARACTER SET utf8mb4
  COLLATE utf8mb4_unicode_ci;

USE fibra_optica_colombia;

-- ------------------------------------------------------------
-- 1. REGIÓN
-- ------------------------------------------------------------
CREATE TABLE region (
    id_region     INT           NOT NULL AUTO_INCREMENT,
    nombre_region VARCHAR(100)  NOT NULL,
    PRIMARY KEY (id_region),
    UNIQUE KEY uq_region_nombre (nombre_region)
) ENGINE=InnoDB COMMENT='Regiones geográficas de Colombia';

-- ------------------------------------------------------------
-- 2. DEPARTAMENTO
-- ------------------------------------------------------------
CREATE TABLE departamento (
    cod_departamento CHAR(2)      NOT NULL,   -- Código DANE
    nombre           VARCHAR(100) NOT NULL,
    id_region        INT          NOT NULL,
    PRIMARY KEY (cod_departamento),
    CONSTRAINT fk_depto_region
        FOREIGN KEY (id_region)
        REFERENCES region (id_region)
        ON UPDATE CASCADE
        ON DELETE RESTRICT
) ENGINE=InnoDB COMMENT='Departamentos de Colombia (código DANE)';

-- ------------------------------------------------------------
-- 3. MUNICIPIO
-- ------------------------------------------------------------
CREATE TABLE municipio (
    cod_municipio    CHAR(5)      NOT NULL,   -- Código DANE municipio
    nombre           VARCHAR(150) NOT NULL,
    cod_departamento CHAR(2)      NOT NULL,
    PRIMARY KEY (cod_municipio),
    CONSTRAINT fk_mun_depto
        FOREIGN KEY (cod_departamento)
        REFERENCES departamento (cod_departamento)
        ON UPDATE CASCADE
        ON DELETE RESTRICT
) ENGINE=InnoDB COMMENT='Municipios de Colombia (código DANE)';

-- ------------------------------------------------------------
-- 4. INDICADOR
-- ------------------------------------------------------------
CREATE TABLE indicador (
    id_indicador INT          NOT NULL,
    descripcion  VARCHAR(255) NULL,
    PRIMARY KEY (id_indicador)
) ENGINE=InnoDB COMMENT='Indicadores de seguimiento del proyecto';

-- ------------------------------------------------------------
-- 5. PROYECTO_FIBRA  (tabla de hechos / principal)
-- ------------------------------------------------------------
CREATE TABLE proyecto_fibra (
    id_proyecto              INT            NOT NULL AUTO_INCREMENT,
    cod_municipio            CHAR(5)        NOT NULL,
    id_indicador             INT            NOT NULL,
    fecha_operacion          DATE           NULL     COMMENT 'Inicio de operación',
    fecha_fin_operacion      DATE           NULL     COMMENT 'Fin de operación',
    estado_actual            VARCHAR(50)    NOT NULL DEFAULT 'En Operación',
    inversion_fontic         DECIMAL(18,2)  NULL     COMMENT 'Inversión FONTIC (COP)',
    inversion_contrapartida  DECIMAL(18,2)  NULL     COMMENT 'Inversión contrapartida (COP)',
    inversion_total          DECIMAL(18,2)  NULL     COMMENT 'Inversión total (COP)',
    fecha_cargue             DATE           NULL     COMMENT 'Fecha de cargue al sistema',
    fecha_vigencia           DATE           NULL     COMMENT 'Fecha de vigencia del registro',
    PRIMARY KEY (id_proyecto),
    CONSTRAINT fk_pf_municipio
        FOREIGN KEY (cod_municipio)
        REFERENCES municipio (cod_municipio)
        ON UPDATE CASCADE
        ON DELETE RESTRICT,
    CONSTRAINT fk_pf_indicador
        FOREIGN KEY (id_indicador)
        REFERENCES indicador (id_indicador)
        ON UPDATE CASCADE
        ON DELETE RESTRICT,
    INDEX idx_pf_estado (estado_actual),
    INDEX idx_pf_fecha_op (fecha_operacion),
    INDEX idx_pf_municipio (cod_municipio)
) ENGINE=InnoDB COMMENT='Registro de proyectos de fibra óptica por municipio';


