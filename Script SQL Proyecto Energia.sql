-- Resumen de inversión por departamento
CREATE OR REPLACE VIEW v_inversion_por_departamento AS
SELECT
    d.cod_departamento,
    d.nombre                        AS departamento,
    r.nombre_region                 AS region,
    COUNT(pf.id_proyecto)           AS total_municipios,
    SUM(pf.inversion_fontic)        AS total_fontic,
    SUM(pf.inversion_contrapartida) AS total_contrapartida,
    SUM(pf.inversion_total)         AS total_inversion
FROM proyecto_fibra pf
JOIN municipio      m  ON pf.cod_municipio    = m.cod_municipio
JOIN departamento   d  ON m.cod_departamento  = d.cod_departamento
JOIN region         r  ON d.id_region         = r.id_region
GROUP BY d.cod_departamento, d.nombre, r.nombre_region;

-- Resumen por región
CREATE OR REPLACE VIEW v_inversion_por_region AS
SELECT
    r.nombre_region,
    COUNT(pf.id_proyecto)           AS total_municipios,
    SUM(pf.inversion_total)         AS total_inversion
FROM proyecto_fibra pf
JOIN municipio    m ON pf.cod_municipio   = m.cod_municipio
JOIN departamento d ON m.cod_departamento = d.cod_departamento
JOIN region       r ON d.id_region        = r.id_region
GROUP BY r.nombre_region;

